#!/usr/bin/env node
/**
 * PLAT.GEN.4 — 扫描 apps/<id>/app.manifest.json 生成 app 注册表。
 *
 *   node scripts/build-app-registry.mjs            # 写 packages/theme/src/generated/appRegistry.js
 *   node scripts/build-app-registry.mjs --check    # 只比对，不一致非零退出（staleness 守卫）
 *
 * manifest 是 single source of truth；siteMeta.js / launcher.js / brand.js /
 * scripts/pwa/apps.config.mjs 是消费生成物的薄壳。starter 是模板本体，不进注册表
 * （其 PWA 调试条目由 apps.config.mjs 手工保留）。
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { loadManifest, listManifestApps } from './lib/app-manifest.mjs'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const OUT = join(ROOT, 'packages', 'theme', 'src', 'generated', 'appRegistry.js')
const CHECK = process.argv.includes('--check')

const ids = listManifestApps(ROOT)
if (!ids.length) {
  console.error('build:app-registry — 找不到任何 app.manifest.json')
  process.exit(1)
}
const manifests = []
let bad = false
for (const id of ids) {
  const { manifest, errors } = loadManifest(ROOT, id)
  if (errors.length) {
    console.error(`✖ ${id} manifest 校验失败：\n  - ${errors.join('\n  - ')}`)
    bad = true
    continue
  }
  manifests.push(manifest)
}
if (bad) process.exit(1)

manifests.sort((a, b) => (a.registryOrder ?? 1e9) - (b.registryOrder ?? 1e9) || a.id.localeCompare(b.id))

const J = (v) => JSON.stringify(v)
/** 对象字面量（2 空格缩进的生成风格） */
function obj(entries, indent) {
  const pad = ' '.repeat(indent)
  return `{\n${entries.map(([k, v]) => `${pad}  ${/^[a-zA-Z_$][\w$]*$/.test(k) ? k : J(k)}: ${v},`).join('\n')}\n${pad}}`
}

const siteMetaEntries = manifests.map((m) => [
  m.id,
  obj(
    [
      ['id', J(m.id)],
      ['name', J(m.name)],
      ['shortName', J(m.shortName)],
      ['description', obj([['zh', J(m.description.zh)], ['en', J(m.description.en)]], 4)],
      ['themeColor', obj([['light', J(m.themeColor.light)], ['dark', J(m.themeColor.dark)]], 4)],
      ['defaultTheme', J(m.defaultTheme ?? 'auto')],
      ['locale', J(m.locale ?? 'zh-CN')],
      ['storageKey', J(m.storageKey)],
      ['storageKind', J(m.storageKind ?? 'nested')],
      ['settingsThemePath', J(m.settingsThemePath ?? ['settings', 'theme'])],
      ['favicon', J(m.favicon ?? { id: 'app-favicon', light: '/favicon-32.png', dark: '/favicon-32.png' })],
      ['manifest', J('/manifest.webmanifest')],
      ['appleTouchIcon', J(m.appleTouchIcon ?? '/apple-touch-icon.png')],
      ['categories', J(m.categories ?? ['utilities'])],
    ],
    2,
  ),
])

const originEntries = manifests.map((m) => [
  m.id,
  `{ production: ${J(`https://${m.domain}`)}, devPort: ${m.devPort} }`,
])

const switcher = manifests
  .filter((m) => m.switcherOrder != null)
  .sort((a, b) => a.switcherOrder - b.switcherOrder || a.id.localeCompare(b.id))
  .map((m) => `  { id: ${J(m.id)}${m.experimental ? ', experimental: true' : ''} },`)

const accentEntries = manifests.map((m) => {
  const a = m.wordmarkAccent ?? { light: '#5b6a79', dark: '#a9b6c4' }
  return [m.id, `{ light: ${J(a.light)}, dark: ${J(a.dark)} }`]
})

const wordmarkBase = manifests.filter((m) => m.wordmarkBase != null).map((m) => [m.id, J(m.wordmarkBase)])
const assetPrefix = manifests.filter((m) => m.brandAssetPrefix != null).map((m) => [m.id, J(m.brandAssetPrefix)])

const pwaEntries = manifests.map((m) => {
  const fields = [
    ['id', J(m.id)],
    ['name', J(m.pwaName ?? m.name)],
    ['workspace', J(m.workspace)],
    ['port', String(m.previewPort ?? m.devPort)],
    ['shellType', J(m.shellType)],
    ['waitSelector', J(m.waitSelector ?? '.app-shell')],
    ...(m.mainQuery != null ? [['mainQuery', J(m.mainQuery)]] : []),
    ['nestedWrapInMain', String(m.shellType === 'main-wrap-main')],
    ['routes', J(m.routes)],
    ...(m.clipPaths != null ? [['clipPaths', J(m.clipPaths)]] : []),
    ['scrollQaPath', J(m.scrollQaPath ?? '/settings')],
    ...(m.moreButton != null ? [['moreButton', J(m.moreButton)]] : []),
    ...(m.moreClose != null ? [['moreClose', J(m.moreClose)]] : []),
    ...(m.authGate ? [['authGate', 'true']] : []),
    ['production', String(m.production === true)],
    ['pwaTestEnabled', String(m.pwaTestEnabled !== false)],
  ]
  return [m.id, obj(fields, 2)]
})

const union = manifests.map((m) => `'${m.id}'`).join(' | ')

const out = `/**
 * GENERATED by scripts/build-app-registry.mjs — DO NOT EDIT.
 * Source of truth: apps/<id>/app.manifest.json（PLAT.GEN.4）。
 * 改 manifest 后运行 npm run build:app-registry。
 */

/** @typedef {${union}} LifeOsAppId */

/**
 * @type {Record<LifeOsAppId, {
 *   id: LifeOsAppId,
 *   name: string,
 *   shortName: string,
 *   description: { zh: string, en: string },
 *   themeColor: { light: string, dark: string },
 *   defaultTheme: 'light' | 'dark' | 'auto',
 *   locale: string,
 *   storageKey: string,
 *   storageKind: 'nested' | 'direct',
 *   settingsThemePath: string[],
 *   favicon: { id?: string, light: string, dark?: string },
 *   manifest: string,
 *   appleTouchIcon: string,
 *   categories: string[]
 * }>}
 */
export const LIFE_OS_SITE_META = ${obj(siteMetaEntries, 0)}

/** @type {Record<LifeOsAppId, { production: string; devPort: number }>} */
export const LIFE_OS_APP_ORIGINS = ${obj(originEntries, 0)}

/**
 * 切换器顺序来自 manifest.switcherOrder（Portal 是启动器 hub，无 switcherOrder 不列入）。
 * @type {Array<{ id: LifeOsAppId; experimental?: boolean }>}
 */
export const LIFE_OS_SWITCHER_APPS = [
${switcher.join('\n')}
]

/** @type {Record<LifeOsAppId, { light: string; dark: string }>} */
export const LIFE_OS_APP_WORDMARK_ACCENT = ${obj(accentEntries, 0)}

/** wordmark 主体覆盖（缺省 = shortName） @type {Partial<Record<LifeOsAppId, string>>} */
export const LIFE_OS_WORDMARK_BASE = ${wordmarkBase.length ? obj(wordmarkBase, 0) : '{}'}

/** 品牌资产路径前缀覆盖 @type {Partial<Record<LifeOsAppId, string>>} */
export const LIFE_OS_BRAND_ASSET_PREFIX = ${assetPrefix.length ? obj(assetPrefix, 0) : '{}'}

/**
 * PWA 调试矩阵原始数据（不含 scroll selector 派生 — 由 scripts/pwa/apps.config.mjs
 * 按 shellType 计算并合成 starter 模板条目）。
 */
export const LIFE_OS_PWA_APPS = ${obj(pwaEntries, 0)}
`

if (CHECK) {
  const current = readFileSync(OUT, 'utf8')
  if (current === out) {
    console.log(`build:app-registry --check OK（${manifests.length} app）`)
  } else {
    console.error('✖ appRegistry.js 与 manifest 不一致 — 运行 npm run build:app-registry')
    process.exit(1)
  }
} else {
  writeFileSync(OUT, out)
  console.log(`build:app-registry OK → ${OUT.slice(ROOT.length + 1)}（${manifests.length} app）`)
}
